#ifndef ROOT_H
#define ROOT_H

/*
 * Тип указателя на функцию одной переменной
 * Используется для передачи функций f(x) и g(x)
 */
typedef double (*func_t)(double x);

/*
 * Тип указателя на функцию производной
 * Используется для метода Ньютона и комбинированного метода
 */
typedef double (*deriv_t)(double x);

/*
 * Функция поиска корня уравнения f(x) = g(x) на отрезке [a, b]
 * Использует комбинированный метод (метод хорд и метод Ньютона)
 *
 * Параметры:
 *   f, g - функции, разность которых ищется
 *   a, b - границы отрезка (должно быть f(a)-g(a) и f(b)-g(b) разных знаков)
 *   df, dg - производные функций f и g (для метода Ньютона)
 *   eps - требуемая точность
 *   iterations - указатель для сохранения числа итераций
 *
 * Возвращает:
 *   приближенное значение корня
 */
double ROOT_root(func_t f, func_t g, double a, double b, deriv_t df, deriv_t dg,
            double eps, int *iterations);

#endif /* ROOT_H */